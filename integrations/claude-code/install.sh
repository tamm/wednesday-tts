#!/usr/bin/env bash
# Install Wednesday TTS Claude Code hooks.
#
# Writes thin stub scripts into ~/.claude/hooks/ that delegate to this repo.
# No symlinks, no pip install required — assumes git clone in place.
# Re-run this script after pulling changes to update the baked-in path.

set -euo pipefail

REPO_DIR="$(cd "$(dirname "$0")/../.." && pwd)"
# On Windows/MSYS the above gives /c/dev/... — convert to C:/dev/... for Python
if [[ "$REPO_DIR" =~ ^/[a-z]/ ]]; then
    REPO_DIR="$(echo "$REPO_DIR" | sed 's|^/\([a-z]\)/|\1:/|')"
fi
HOOKS_DIR="$HOME/.claude/hooks"
INTEGRATION_DIR="$REPO_DIR/integrations/claude-code"

mkdir -p "$HOOKS_DIR"

write_stub() {
    local target_script="$1"   # absolute path to the real script in the repo
    local stub_path="$2"       # where to write the stub in ~/.claude/hooks/
    local name
    name="$(basename "$stub_path")"

    if [ ! -f "$target_script" ]; then
        echo "WARNING: source not found, skipping: $target_script"
        return
    fi

    # If $stub_path is a symlink (from a prior install that linked directly
    # to the source), delete it first. cat > symlink follows the link and
    # would clobber the source file — this bit us once, never again.
    if [ -L "$stub_path" ]; then
        rm -f "$stub_path"
    fi

    cat > "$stub_path" <<STUB
#!/usr/bin/env python3
# Generated by integrations/claude-code/install.sh — re-run to update.
import sys
sys.path.insert(0, "$REPO_DIR/src")
sys.path.insert(0, "$INTEGRATION_DIR")
exec(open("$target_script").read())
STUB

    chmod +x "$stub_path"
    echo "Wrote stub: $name -> $target_script"
}

copy_file() {
    local src="$1"
    local dst="$2"
    local name
    name="$(basename "$dst")"

    if [ ! -f "$src" ]; then
        echo "WARNING: source not found, skipping: $src"
        return
    fi

    cp "$src" "$dst"
    echo "Copied: $name"
}

# Hook stubs (thin wrappers that exec the real script with repo on sys.path)
write_stub "$INTEGRATION_DIR/speak-response.py"   "$HOOKS_DIR/speak-response.py"
write_stub "$INTEGRATION_DIR/pre-tool-speak.py"   "$HOOKS_DIR/pre-tool-speak.py"

# tts_platform.py — kept as a stub for backward compat with any code that imports it
write_stub "$REPO_DIR/src/wednesday_tts/platform.py" "$HOOKS_DIR/tts_platform.py"

# Data files — just copies (they don't change often; re-run install to update)
copy_file "$REPO_DIR/data/tts-dictionary.json"   "$HOOKS_DIR/tts-dictionary.json"
copy_file "$REPO_DIR/data/tts-filenames.json"    "$HOOKS_DIR/tts-filenames.json"

# Stop script — copy the shell script directly
copy_file "$REPO_DIR/scripts/stop-tts.sh"        "$HOOKS_DIR/stop-tts.sh"
chmod +x "$HOOKS_DIR/stop-tts.sh"

echo ""
echo "Done. REPO_DIR: $REPO_DIR"
echo "Make sure ~/.claude/settings.json has the hooks registered."
echo "See integrations/claude-code/README.md for the settings.json snippet."

# Warn if TTS server isn't reachable
if command -v curl &>/dev/null; then
    curl -sf --max-time 2 http://localhost:5678/health &>/dev/null \
        && echo "TTS server is running." \
        || echo "WARNING: TTS server not running on localhost:5678. Start it before using hooks."
fi
